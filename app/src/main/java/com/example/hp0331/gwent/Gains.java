public class Gains {
    public int gain;
    public int attacker;
    public int defender;
    public int attackleft;
    public int defenderleft;

    public Gains(int gain, int attacker, int defender, int attackleft, int defenderleft) {
        this.gain = gain;
        this.attacker = attacker;
        this.defender = defender;
        this.attackleft = attackleft;
        this.defenderleft = defenderleft;
    }


}
